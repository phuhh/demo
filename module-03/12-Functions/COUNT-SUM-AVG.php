COUNT() - Đếm số dòng

SELECT COUNT(column_name)
FROM table_name
WHERE condition;



SUM - Tính tổng các giá trị

SELECT SUM(column_name)
FROM table_name
WHERE condition;



AVG - Tính trung bình các giá trị

SELECT AVG(column_name)
FROM table_name
WHERE condition;