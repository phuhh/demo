Các loại cơ sở dữ liệu

1. Relational Database Management System - RDBMS (structured with SQL) - CSDL quan hệ (Oracle, Mysql, SQL Server, PostgreSQL)
2. NoSQL (flexible for unstructured data) - CSDL hướng tài liệu (MongoDB, Couchbase)
3. CSDL dạng khoá - giá trị (Redis, Memcached)
4. CSDL phi quan hệ (Cassandra, HBase)
5. CSDL dạng bộ máy tìm kiếm (Elasticsearch)



SQL : Ngôn ngữ truy vấn => sẽ không bao giờ thay đổi

MYSQL, SQL Server : Hệ quản trị CSDL (phần mềm, thường xuyên update phiên bản)