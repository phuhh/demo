<?php
/* Toán tử gán kết hợp toán tử số học  */

/** 
 * Toán tử += (Cộng Bằng - phép tính cộng và gán) 
 */
$a = 20;
$a += 10; // Tương đương với $a = 20 + 10
print_r($a); //OUTPUT: 30
echo '<br>';

/** 
 * Toán tử -= (Trừ Bằng - phép tính trừ và gán) 
 */
$a = 20;
$a -= 10; // Tương đương với $a = 20 - 10
print_r($a); //OUTPUT: 10
echo '<br>';

/** 
 * Toán tử *= (Nhân Bằng - phép tính nhân và gán) 
 */
$a = 10;
$a *= 5; // Tương đương với $a = 10 * 5
print_r($a); //OUTPUT: 50
echo '<br>';

/** 
 * Toán tử /= (Chia Bằng - phép tính chia và gán) 
 */
$a = 10;
$a /= 5; // Tương đương với $a = 10 / 5
print_r($a); //OUTPUT: 2
echo '<br>';

/** 
 * Toán tử %= (Chia lấy dư Bằng - phép chia lấy dư và gán) 
 */
$a = 10;
$a %= 3; // Tương đương với $a = 10 / 3
print_r($a); //OUTPUT: 1
echo '<br>';
